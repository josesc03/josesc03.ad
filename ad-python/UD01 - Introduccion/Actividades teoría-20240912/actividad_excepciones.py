# EXCEPCIONES: se producen cuando ocurre un error de ejecución en un programa.
# el programa acaba y Python muestra un mensaje de error.
# Podemos manejar las excepciones que se producen en la ejecuación de un programa
# y dar una respuesta personalizada -> try ... except

try:
    dato = int(input("Introduce un entero: "))
    dato = 100 / dato
except ValueError:
    print("Error. El dato no es un entero.")
except ZeroDivisionError:
    print("Error. El dato debe ser distinto de cero.")
except:
    print("\nError genérico.")
else:
    print("El resultado es:", dato)
finally:
    print("Fin del programa.")


